export async function getUserBalances (moralis) {
    const user = await moralis.User.current();
    if (!user) {
        return
    }
    const userAddress = user.get('ethAddress');

    window.BTDMDContract =  new web3.eth.Contract(BTDMDContractAbi, BTDMD_CONTRACT_ADDRESS)
    window.stakingContract = new web3.eth.Contract(stakingContractAbi, STAKING_CONTRACT_ADDRESS);
    // User BTDMDBalance:
    var userBTDMDBalance = await BTDMDContract.methods.balanceOf(userAddress).call()/(10**8);
    console.log(userBTDMDBalance);
    // User StakedBalance
    var userStakedBalance = await stakingContract.methods.stakeOf(userAddress).call()/(10**8);
    console.log(userStakedBalance);

    return { userBTDMDBalance, userStakedBalance };
}

// Function to call on STAKE
export async function stakeAmount(moralis, amount) {
    const user = await moralis.User.current();
    var amountToStake = (amount * (10**8)) // this needs to be multipled by 10**8 as solidity holds all amounts as integer and this has 8 decimal places. 
    const userAddress = user.get('ethAddress');
    window.BTDMDContract =  new web3.eth.Contract(BTDMDContractAbi, BTDMD_CONTRACT_ADDRESS)
    window.stakingContract = new web3.eth.Contract(stakingContractAbi, STAKING_CONTRACT_ADDRESS);
    const allowance = await BTDMDContract.methods.allowance(userAddress, STAKING_CONTRACT_ADDRESS).call();
    if (allowance < amountToStake){
        await BTDMDContract.methods.approve(STAKING_CONTRACT_ADDRESS, 999999999999999).send({from: userAddress});
    }
    const receipt = await stakingContract.methods.stakeBTDMD(amountToStake).send({from: ethereum.selectedAddress});
    console.log(receipt);
    return receipt;
}

// Function to call on UNSTAKE
export async function unstakeAmount(moralis, amount) {
    const user = await moralis.User.current();
    if (!user) {
        return
    }
    var amountToUnstake = (amount * (10**8)) // this needs to be multipled by 10**8 as solidity holds all amounts as integer and this has 8 decimal places. 
    const userAddress = user.get('ethAddress');
    window.BTDMDContract =  new web3.eth.Contract(BTDMDContractAbi, BTDMD_CONTRACT_ADDRESS)
    window.stakingContract = new web3.eth.Contract(stakingContractAbi, STAKING_CONTRACT_ADDRESS);
    const allowance = await BTDMDContract.methods.allowance(userAddress, STAKING_CONTRACT_ADDRESS).call();
    if (allowance < amountToUnstake){
        await BTDMDContract.methods.approve(STAKING_CONTRACT_ADDRESS, 999999999999999).send({from: userAddress});
    }
    const receipt = await stakingContract.methods.unStakeBTDMD(amountToUnstake).send({from: ethereum.selectedAddress});
    console.log(receipt);
    return receipt
}


// Function to call on claim button press in Trove
export async function claimTrove(moralis, ids) {
    const metadataUrl = ids.join(",");
    console.log({metadataUrl})
    window.web3 = await moralis.Web3.enable();
    window.claimContract = new web3.eth.Contract(claimContractAbi, CLAIM_CONTRACT_ADDRESS);
    const receipt = await claimContract.methods.recordClaim(ethereum.selectedAddress, metadataUrl).send({from: ethereum.selectedAddress});
    console.log(receipt);
    return receipt
}


